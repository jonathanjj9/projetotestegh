package projetoTesteGH;

 /* Calcular volume de uma lata usando a seguinte formula: raio ao quadrado  * pi  * altura */

public class b04volumeLata {

	public static void main(String args[]) {
		
		double volume;
		double r = 1.2; 
		double altura = 2;
		
		volume = 3.14 * (r*r) * altura;
		
		System.out.println("Volume da lata: " + volume);
		
		
		
		
		}	
}

package projetoTesteGH;

public class b28reajusteUm {

	/**Fazer um algoritmo que possa entrar com o saldo de uma aplicação e imprima o 
	novo saldo, considerando o reajuste de 1 %. */
	
	public static void main(String[] args) {
		
		double saldoAplicacao = 500;
		double valorPorcentagem = 1;
		double percentual = (saldoAplicacao*valorPorcentagem)/100;
		
		System.out.println(percentual);
		
		
	}
	
	
}
